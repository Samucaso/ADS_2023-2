
public class TesteOperadoresMatematicos1 {

	public static void main(String[] args) {
		int a = 1;
		a++;
		
		int b = 4;
		b--;
		
		int c = 5;
		c *= 2;
		
		int d = 30;
		d /= 2; 
		
		int e = 10;
		e %= 2;
		
		System.out.println("a="+a);
		System.out.println("b="+b);
		System.out.println("c="+c);
		System.out.println("d="+d);
		System.out.println("e="+e);
		
	}

}
 